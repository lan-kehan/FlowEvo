#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/../../.." && pwd)"

FLOW_TTS_SAMPLE_SIZE="${FLOW_TTS_SAMPLE_SIZE:-500}"
FLOW_TTS_SAMPLE_SEED="${FLOW_TTS_SAMPLE_SEED:-42}"
FLOW_TTS_SPLIT="${FLOW_TTS_SPLIT:-train}"
FLOW_TTS_BETAS="${FLOW_TTS_BETAS:-0 0.25 0.5 0.75 1.0}"
FLOW_TTS_BUDGET="${FLOW_TTS_BUDGET:-64}"
FLOW_TTS_NUM_STEPS="${FLOW_TTS_NUM_STEPS:-10}"
FLOW_TTS_RESOLUTION="${FLOW_TTS_RESOLUTION:-512}"
FLOW_TTS_GUIDANCE_SCALE="${FLOW_TTS_GUIDANCE_SCALE:-4.5}"
FLOW_TTS_NOISE_LEVEL="${FLOW_TTS_NOISE_LEVEL:-0.7}"
FLOW_TTS_SDE_TYPE="${FLOW_TTS_SDE_TYPE:-sde}"
FLOW_TTS_DATASET="${FLOW_TTS_DATASET:-${REPO_ROOT}/flow_grpo/dataset/pickscore}"
FLOW_TTS_MODEL="${FLOW_TTS_MODEL:-${REPO_ROOT}/SD_3.5_med}"
FLOW_TTS_PICKSCORE_MODEL="${FLOW_TTS_PICKSCORE_MODEL:-${REPO_ROOT}/PickScore_v1}"
FLOW_TTS_DEVICE="${FLOW_TTS_DEVICE:-}"
FLOW_TTS_TEXT_ENCODER_DEVICE="${FLOW_TTS_TEXT_ENCODER_DEVICE:-}"
FLOW_TTS_SCORE_DEVICE="${FLOW_TTS_SCORE_DEVICE:-}"
FLOW_TTS_DTYPE="${FLOW_TTS_DTYPE:-}"
FLOW_TTS_SCORE_DTYPE="${FLOW_TTS_SCORE_DTYPE:-float32}"
FLOW_TTS_NUM_SHARDS="${FLOW_TTS_NUM_SHARDS:-1}"
FLOW_TTS_SHARD_INDEX="${FLOW_TTS_SHARD_INDEX:-0}"
FLOW_TTS_EVAL_DEVICES="${FLOW_TTS_EVAL_DEVICES:-}"
FLOW_TTS_EVAL_TEXT_ENCODER_DEVICES="${FLOW_TTS_EVAL_TEXT_ENCODER_DEVICES:-${FLOW_TTS_EVAL_DEVICES}}"
FLOW_TTS_EVAL_SCORE_DEVICES="${FLOW_TTS_EVAL_SCORE_DEVICES:-${FLOW_TTS_EVAL_DEVICES}}"
FLOW_TTS_SHARD_OUTPUT_DIR="${FLOW_TTS_SHARD_OUTPUT_DIR:-}"
FLOW_TTS_LOCAL_ONLY_FLAG="${FLOW_TTS_LOCAL_ONLY_FLAG:-}"
FLOW_TTS_PROGRESS_FLAG="${FLOW_TTS_PROGRESS_FLAG:-}"
FLOW_TTS_COMPACT_FLAG="${FLOW_TTS_COMPACT_FLAG:-}"
FLOW_TTS_OFFLOAD_TEXT_ENCODERS_FLAG="${FLOW_TTS_OFFLOAD_TEXT_ENCODERS_FLAG:-}"
FLOW_TTS_PROMPT_PROFILE="${FLOW_TTS_PROMPT_PROFILE:-legacy}"

case "${FLOW_TTS_PROMPT_PROFILE}" in
  legacy)
    WORKFLOW_PROMPT_PATH_DEFAULT="${SCRIPT_DIR}/prompts/proposer_prompt.txt"
    ;;
  autotts|strict)
    WORKFLOW_PROMPT_PATH_DEFAULT="${SCRIPT_DIR}/prompts/proposer_prompt_autotts.txt"
    ;;
  *)
    echo "[flow_autotts workflow] unknown FLOW_TTS_PROMPT_PROFILE=${FLOW_TTS_PROMPT_PROFILE}; expected legacy, autotts, or strict" >&2
    exit 2
    ;;
esac

WORKFLOW_RESULT_DIR_DEFAULT="${REPO_ROOT}/logs/flow_autotts/pickscore_sd35/training_results"
WORKFLOW_RESULT_DIR_EFFECTIVE="${WORKFLOW_RESULT_DIR:-${WORKFLOW_RESULT_DIR_DEFAULT}}"

if [[ -n "${FLOW_TTS_EVAL_DEVICES}" ]]; then
  FLOW_TTS_SHARD_OUTPUT_DIR="${FLOW_TTS_SHARD_OUTPUT_DIR:-${WORKFLOW_RESULT_DIR_EFFECTIVE}/shards}"
  WORKFLOW_EVAL_CMD_DEFAULT="python -m flow_autotts.experiments.pickscore_sd35.parallel_eval --devices '${FLOW_TTS_EVAL_DEVICES}' --text-encoder-devices '${FLOW_TTS_EVAL_TEXT_ENCODER_DEVICES}' --score-devices '${FLOW_TTS_EVAL_SCORE_DEVICES}' --shard-output-dir ${FLOW_TTS_SHARD_OUTPUT_DIR} --dataset ${FLOW_TTS_DATASET} --split ${FLOW_TTS_SPLIT} --sample-size ${FLOW_TTS_SAMPLE_SIZE} --sample-seed ${FLOW_TTS_SAMPLE_SEED} --rounds 1 --controllers optimal --betas ${FLOW_TTS_BETAS} --budget ${FLOW_TTS_BUDGET} --output ${WORKFLOW_RESULT_DIR_EFFECTIVE}/history.json --summary-output ${WORKFLOW_RESULT_DIR_EFFECTIVE}/summary.json --model ${FLOW_TTS_MODEL} --pickscore-model ${FLOW_TTS_PICKSCORE_MODEL} --num-steps ${FLOW_TTS_NUM_STEPS} --resolution ${FLOW_TTS_RESOLUTION} --guidance-scale ${FLOW_TTS_GUIDANCE_SCALE} --noise-level ${FLOW_TTS_NOISE_LEVEL} --sde-type ${FLOW_TTS_SDE_TYPE} --score-dtype ${FLOW_TTS_SCORE_DTYPE} ${FLOW_TTS_LOCAL_ONLY_FLAG} ${FLOW_TTS_PROGRESS_FLAG} ${FLOW_TTS_COMPACT_FLAG} ${FLOW_TTS_OFFLOAD_TEXT_ENCODERS_FLAG}"
  if [[ -n "${FLOW_TTS_DTYPE}" ]]; then
    WORKFLOW_EVAL_CMD_DEFAULT="${WORKFLOW_EVAL_CMD_DEFAULT} --dtype ${FLOW_TTS_DTYPE}"
  fi
else
  WORKFLOW_EVAL_CMD_DEFAULT="python -m flow_autotts.experiments.pickscore_sd35.harness --dataset ${FLOW_TTS_DATASET} --split ${FLOW_TTS_SPLIT} --sample-size ${FLOW_TTS_SAMPLE_SIZE} --sample-seed ${FLOW_TTS_SAMPLE_SEED} --num-shards ${FLOW_TTS_NUM_SHARDS} --shard-index ${FLOW_TTS_SHARD_INDEX} --rounds 1 --controllers optimal --betas ${FLOW_TTS_BETAS} --budget ${FLOW_TTS_BUDGET} --output ${WORKFLOW_RESULT_DIR_EFFECTIVE}/history.json --summary-output ${WORKFLOW_RESULT_DIR_EFFECTIVE}/summary.json --model ${FLOW_TTS_MODEL} --pickscore-model ${FLOW_TTS_PICKSCORE_MODEL} --num-steps ${FLOW_TTS_NUM_STEPS} --resolution ${FLOW_TTS_RESOLUTION} --guidance-scale ${FLOW_TTS_GUIDANCE_SCALE} --noise-level ${FLOW_TTS_NOISE_LEVEL} --sde-type ${FLOW_TTS_SDE_TYPE} --score-dtype ${FLOW_TTS_SCORE_DTYPE} ${FLOW_TTS_LOCAL_ONLY_FLAG} ${FLOW_TTS_PROGRESS_FLAG} ${FLOW_TTS_COMPACT_FLAG} ${FLOW_TTS_OFFLOAD_TEXT_ENCODERS_FLAG}"

  if [[ -n "${FLOW_TTS_DEVICE}" ]]; then
    WORKFLOW_EVAL_CMD_DEFAULT="${WORKFLOW_EVAL_CMD_DEFAULT} --device ${FLOW_TTS_DEVICE}"
  fi
  if [[ -n "${FLOW_TTS_TEXT_ENCODER_DEVICE}" ]]; then
    WORKFLOW_EVAL_CMD_DEFAULT="${WORKFLOW_EVAL_CMD_DEFAULT} --text-encoder-device ${FLOW_TTS_TEXT_ENCODER_DEVICE}"
  fi
  if [[ -n "${FLOW_TTS_SCORE_DEVICE}" ]]; then
    WORKFLOW_EVAL_CMD_DEFAULT="${WORKFLOW_EVAL_CMD_DEFAULT} --score-device ${FLOW_TTS_SCORE_DEVICE}"
  fi
  if [[ -n "${FLOW_TTS_DTYPE}" ]]; then
    WORKFLOW_EVAL_CMD_DEFAULT="${WORKFLOW_EVAL_CMD_DEFAULT} --dtype ${FLOW_TTS_DTYPE}"
  fi
fi

export WORKFLOW_RESUME="${WORKFLOW_RESUME:-1}"
export WORKFLOW_WORKDIR="${WORKFLOW_WORKDIR:-${REPO_ROOT}}"
export WORKFLOW_METHOD_FILE="${WORKFLOW_METHOD_FILE:-flow_autotts/controllers/optimal.py}"
export WORKFLOW_TEMPLATE_FILE="${WORKFLOW_TEMPLATE_FILE:-flow_autotts/controllers/optimal.template.py}"
export WORKFLOW_HISTORY_DIR="${WORKFLOW_HISTORY_DIR:-logs/flow_autotts/pickscore_sd35/history}"
export WORKFLOW_CODEX_LOG_PARENT="${WORKFLOW_CODEX_LOG_PARENT:-${REPO_ROOT}/logs/flow_autotts/pickscore_sd35/codex_logs}"
export WORKFLOW_RESULT_DIR="${WORKFLOW_RESULT_DIR_EFFECTIVE}"
export WORKFLOW_PROMPT_PATH="${WORKFLOW_PROMPT_PATH:-${WORKFLOW_PROMPT_PATH_DEFAULT}}"
export WORKFLOW_ROUNDS="${WORKFLOW_ROUNDS:-5}"
export WORKFLOW_CONTEXT_HISTORY_ROUNDS="${WORKFLOW_CONTEXT_HISTORY_ROUNDS:-${WORKFLOW_ROUNDS}}"
export WORKFLOW_EVAL_CMD="${WORKFLOW_EVAL_CMD:-${WORKFLOW_EVAL_CMD_DEFAULT}}"
export WORKFLOW_EVAL_CWD="${WORKFLOW_EVAL_CWD:-${REPO_ROOT}}"
export WORKFLOW_EVAL_TIMEOUT_SEC="${WORKFLOW_EVAL_TIMEOUT_SEC:-21600}"
export CODEX_BIN="${CODEX_BIN:-codex}"
export CODEX_EXEC_ARGS="${CODEX_EXEC_ARGS:---dangerously-bypass-approvals-and-sandbox}"

echo "[flow_autotts workflow] REPO_ROOT=${REPO_ROOT}"
echo "[flow_autotts workflow] WORKFLOW_METHOD_FILE=${WORKFLOW_METHOD_FILE}"
echo "[flow_autotts workflow] WORKFLOW_HISTORY_DIR=${WORKFLOW_HISTORY_DIR}"
echo "[flow_autotts workflow] WORKFLOW_RESULT_DIR=${WORKFLOW_RESULT_DIR}"
echo "[flow_autotts workflow] FLOW_TTS_PROMPT_PROFILE=${FLOW_TTS_PROMPT_PROFILE}"
echo "[flow_autotts workflow] WORKFLOW_PROMPT_PATH=${WORKFLOW_PROMPT_PATH}"
echo "[flow_autotts workflow] WORKFLOW_ROUNDS=${WORKFLOW_ROUNDS}"
echo "[flow_autotts workflow] WORKFLOW_CONTEXT_HISTORY_ROUNDS=${WORKFLOW_CONTEXT_HISTORY_ROUNDS}"
echo "[flow_autotts workflow] FLOW_TTS_SAMPLE_SIZE=${FLOW_TTS_SAMPLE_SIZE}"
echo "[flow_autotts workflow] FLOW_TTS_BETAS=${FLOW_TTS_BETAS}"
echo "[flow_autotts workflow] FLOW_TTS_EVAL_DEVICES=${FLOW_TTS_EVAL_DEVICES}"
echo "[flow_autotts workflow] CODEX_EXEC_ARGS=${CODEX_EXEC_ARGS}"
echo "[flow_autotts workflow] WORKFLOW_EVAL_CMD=${WORKFLOW_EVAL_CMD}"

cd "${REPO_ROOT}"
python -m flow_autotts.workflow
